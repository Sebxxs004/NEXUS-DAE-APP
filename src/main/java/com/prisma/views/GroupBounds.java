package com.prisma.views;

final class GroupBounds {
    final double minX;
    final double minY;
    final double width;
    final double height;

    GroupBounds(double minX, double minY, double width, double height) {
        this.minX = minX;
        this.minY = minY;
        this.width = width;
        this.height = height;
    }
}
